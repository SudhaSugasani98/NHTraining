using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace SudhaSugasaniLibrary
{
    public class Chemistry
    {
        /// <summary>
        /// This method will return the formula for Benzene
        /// </summary>
        /// <returns>String</returns>
        public static string GetBenzene()
        {
            return "C6H6";
        }
        /// <summary>
        /// This method will return the formula for Water
        /// </summary>
        /// <returns>String</returns>
        public static string GetWater()
        {
            return "H2O";
        }
        /// <summary>
        /// This method will return the formula for Methane
        /// </summary>
        /// <returns>String</returns>
        public static string GetMethane()
        {
            return "CH4";
        }
    }
}
